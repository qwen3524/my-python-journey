# hello_world_broken.py - КОД С ОШИБКАМИ

# Ошибка 1: Пропущена скобка в конце функции
print("Привет, начинающий программист!")

# Ошибка 2: Неправильное имя функции
Name = input("Введите ваше имя: ")

# Ошибка 3: Забыт префикс 'f' для f-строки
print("\nДобро пожаловать, {Name}!")

# Ошибка 4: Использование неопределенного модуля
import datetime

# Ошибка 5: Неправильный синтаксис импорта (должно быть в начале)
today = datetime.datetime.now().strftime("%d.%m.%Y")
print(f"Сегодняшняя дата: {today}")

# Ошибка 6: Попытка выполнить команду за пределами строки
print("Спасибо за участие!")
print("Конец.")